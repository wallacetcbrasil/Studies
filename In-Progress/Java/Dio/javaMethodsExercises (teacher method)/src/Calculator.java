/**
 * Example class of the exercise of the first class of methods.
 */

public class Calculator {
    
    public static void funcSum(double num1, double num2) {

        double result = num1 + num2;

        System.out.println("The sum of " + num1 + " and " + num2 + " is " + result);
    }

    public static void funcSubtraction(double num1, double num2) {

        double result = num1 - num2;

        System.out.println("The Subtraction of " + num1 + " and " + num2 + " is " + result);
    }

    public static void funcMultiplication(double num1, double num2) {

        double result = num1 * num2;

        System.out.println("The Multiplication of" + num1 + " and " + num2 + " is " + result);
    }

    public static void funcDivision(double num1, double num2) {

        double result = num1 / num2;

        System.out.println("The Division of" + num1 + " and " + num2 + " is " + result);
    }
}
